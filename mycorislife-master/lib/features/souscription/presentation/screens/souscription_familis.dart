  void _nextStep() {
    // Include payment step: clients 0..3, commercials 0..4
    final maxStep = _isCommercial ? 4 : 3;
    if (_currentStep < maxStep) {
      bool canProceed = false;

      if (_isCommercial) {
        // Pour les commerciaux: step 0 = infos client, step 1 = param√®tres, step 2 = informations, step 3 = recap
        if (_currentStep == 0 && _validateStepClientInfo()) {
          canProceed = true;
        } else if (_currentStep == 1 && _validateStep1()) {
          canProceed = true;
        } else if (_currentStep == 2 && _validateStep2()) {
          canProceed = true;
          _calculatePrime();
        } else if (_currentStep == 3) {
          // Recap pour commercial -> allow moving to payment
          canProceed = true;
        }
      } else {
        // Pour les clients: step 0 = param√®tres, step 1 = informations, step 2 = recap
        if (_currentStep == 0 && _validateStep1()) {
          canProceed = true;
        } else if (_currentStep == 1 && _validateStep2()) {
          canProceed = true;
          _calculatePrime();
        } else if (_currentStep == 2) {
          // Recap pour client -> allow moving to payment
          canProceed = true;
        }
      }

      if (canProceed) {
        setState(() => _currentStep++);
        _progressController.forward();
        _animationController.reset();
        _animationController.forward();

        _pageController.nextPage(
          duration: const Duration(milliseconds: 400),
          curve: Curves.easeInOutCubic,
        );
      }
    }
  }

  void _previousStep() {
    if (_currentStep > 0) {
      setState(() => _currentStep--);
      _progressController.reverse();
      _animationController.reset();
      _animationController.forward();

      _pageController.previousPage(
        duration: const Duration(milliseconds: 400),
        curve: Curves.easeInOutCubic,
      );
    }
  }

  bool _validateStepClientInfo() {
    if (_clientNomController.text.trim().isEmpty) {
      _showErrorSnackBar('Veuillez saisir le nom du client');
      return false;
    }
    if (_clientPrenomController.text.trim().isEmpty) {
      _showErrorSnackBar('Veuillez saisir le pr√©nom du client');
      return false;
    }
    if (_clientDateNaissance == null) {
      _showErrorSnackBar('Veuillez saisir la date de naissance du client');
      return false;
    }
    final maintenant = DateTime.now();
    _clientAge = maintenant.year - _clientDateNaissance!.year;
    if (maintenant.month < _clientDateNaissance!.month ||
        (maintenant.month == _clientDateNaissance!.month &&
            maintenant.day < _clientDateNaissance!.day)) {
      _clientAge = (_clientAge ?? 0) - 1;
    }
    if (_clientAge == null || _clientAge! < 18 || _clientAge! > 65) {
      _showErrorSnackBar(
          '√Çge du client non valide (18-65 ans requis). √Çge calcul√©: ${_clientAge ?? 0} ans');
      return false;
    }
    if (_clientEmailController.text.trim().isEmpty) {
      _showErrorSnackBar('Veuillez saisir l\'email du client');
      return false;
    }
    if (_clientTelephoneController.text.trim().isEmpty) {
      _showErrorSnackBar('Veuillez saisir le t√©l√©phone du client');
      return false;
    }
    // Utiliser l'√¢ge du client pour le calcul
    _age = _clientAge;
    return true;
  }

